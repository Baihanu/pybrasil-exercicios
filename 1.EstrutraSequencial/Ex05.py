"""
Faça um Programa que converta metros para centímetros.
"""
metros = float(input('Por favor, informe a quantidade de metros a ser convertida: '))
centimetros = (metros * 100)
if metros == 1:
    print(f'{metros} metro corresponde à {centimetros} centimetros')
elif metros < 1:
    print(f'{metros} metros corresponde à {centimetros} centimetros')
elif metros > 1:
    print(f'{metros} metros corresponde à {centimetros} centimetros')
